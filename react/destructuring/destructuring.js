// destructuring an array 

let colors = ['blue','red','purple'];

// Destructure here
let [color1, color2, color3] = colors;

console.log(color1, color2, color3);

// destructuring an object 

let planets = { x: 'Saturn', y: 'Mars', z: 'Neptune' };

// Destructure here
let {x, y, z} = planets; 

console.log(x, y, z);